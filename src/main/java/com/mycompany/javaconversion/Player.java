public class Player {

    // fields
    private String name;
    private int number;
    
    // constructor
    public Player(String name, int number){
    this.name = name;
    this.number = number;}
    
    // getters and setters for name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
    // getters and setters for number
    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
    
         
}
